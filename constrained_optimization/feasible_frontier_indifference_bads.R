ppf <- function(x, slope = 1/64, bary = 4) {
  bary - slope *(16-x)^2
}

uFn <- function(x, y, alpha = 0.4){
  ((16-x)^alpha)*(y^(1-alpha))
}

indiffA <- function(x, ubar = uFn(8,3) - 1, alpha = 0.4) {
  (ubar/ ((16-x)^alpha))^(1/ (1 - alpha))
}

tangentLine <- function(x, slope, intercept){
